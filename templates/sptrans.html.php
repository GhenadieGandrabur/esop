    <h1>USEFUL EXPRESSIONS FOR FCE SPEAKING</h1>
    <h1 style="color:red"> PART 1 – INTRODUCING YOURSELF</h1>
    <ul>
<li>I’m speaking English for my own satisfaction / my job / in order to improve my employment prospects.</li>
<li>If you want to go ahead these days, you need to have a good command of English.</li>
<li>I’ve always loved (the sea).</li>
<li>I’d love to visit places untouched by man.</li>
<li>However, there’s no place like home.</li>
<li>That’s a big question!</li>
<li>I’m keen on…</li>
<li>I’m an avid (traveler, swimmer)</li>
<li>I haven’t given it much thought until now.</li>
    </ul>
     <h1 style="color:red">PART 2 – DESCRIBING PHOTOS</h1>
     <h1>Describing the photographs</h1> 
<ul>
<li>The first photograph shows a woman reading a book.</li>
<li>In the background we can see a family sitting around a table outside a café.</li>
<li>What strikes me about the first picture is the fact that…</li>
<li>This picture reminds me of…</li>
<li>Judging from the photograph, the children…</li>
<li>The man appears to be…</li>
<li>From what we can see here, he must …</li>
<li>When you give it a closer look it reveals…</li>
</ul>
<h3>Comparing the photographs</h3>
<ul>
<li>Both pictures are about being a parent.</li>
<li>I think the common theme here is communication.</li>
<li>There are many </li>
<li>Both pictures depict…</li>
<li>Both pictures are quite similar in that they show…</li>
</ul>
<h3>Contrasting</h3>
<ul>
<li>In contrast, the people in the second image look worried.</li>
<li>On the other hand, the scene looks much more modern.</li>
<li>Unlike the first photo, this looks like a happy occasion.</li>
<li>The first picture … whereas the other picture…</li>
<li>Another important difference is…</li>
<li>Picture... doesn’t show ... as clearly as…</li>
<li>Picture … attempts to … but I think picture … does this more effectively </li>
<li>All pictures are interesting, but… gives a stronger idea of…</li>
</ul>




<h3>Speculating</h3>
<ul>
<li>Perhaps they are celebrating something.</li>
<li>Maybe the reason they look so serious is because…</li>
<li>She might be a busy working mother.</li>
<li>They must be working very hard.</li>
<li>It’s difficult to tell from a photograph but this picture could have been taken in…</li>
<li>I might be wrong, but I’d say that this </li>
<li>I would imagine/guess that this picture comes </li>
</ul>
<h3>Personal feelings</h3>
<ul>
<li>I wouldn’t like to be in their shoes</li>
<li>The first photo reminds me of my own </li>
<li>Personally, I’d love to visit a place like that</li>
<li>The following phrases are for helping you while you speak.</li>
<li>I’m surprised thee is no picture showing…</li>
<li>A picture of … would get the message across more effectively
<li>The cover would be so much better if we had a picture of </li>
<li>So let’s look at the alternatives…</li>
<li>Picture … is by far the best to show because …</li>
<li>To me, the picture here… is essential for this kind of publication</li>
</ul>

<h3>Rephrasing</h3>
<ul>
<li>What I really mean is that…</li>
<li>I take that back</li>
<li>What I am trying to say is that …</li>
</ul>
<h3>Hesitators (gaining time)</h3>
<ul>
<li>Well, …</li>
<li>Hmmm… let me see</li>
<li>What else (can I say?)…</li>
<li>Is there anything I can add to that?</li>
<li>I’m not quite sure, but I think…</li>
<li>I haven’t thought about it before, but perhaps…</li>
<li>I don’t really know, but I imagine…</li>
</ul>
